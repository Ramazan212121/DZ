			<h2>В России</h2>

			<p>В 2005 году у Ламборгини появляется официальный дилер в России и было продано два автомобиля. В апреле 2006 года компания открывает свой первый автосалон и продаёт по итогам года 14 экземпляров. В 2007 году продажи увеличиваются до 16 машин. На следующий год было реализовано только 13 автомобилей компании. В 2009 году было продано 9 машин, а в 2010 — на единицу больше. В 2011 году Ламборгини уходит с российского рынка, так как официальный дилер марки не стал продлевать контракт из-за несопоставимости затрат на реализацию и результатов продаж.</p>

			<p>Позже, в апреле 2012 года, возвращается в Россию с новым официальным дистрибьютором. По итогам года было продано 6 автомобилей. 10 экземпляров стали результатом продаж 2013 года. В 2014 году продажи выросли до 26 автомобилей; тогда как в 2015 году было реализовано 19 автомобилей. В 2016 году уже в первом квартале были проданы все 25 автомобилей выделенные по квоте.</p>
		
