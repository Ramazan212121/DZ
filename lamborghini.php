			<p>Automobili Lamborghini S.p.A.  — итальянская компания, производитель дорогих спортивных автомобилей под маркой Lamborghini. Находится в коммуне Сант-Агата-Болоньезе, около Болоньи. Компания основана в 1963 году Ферруччо Ламборгини, на тот момент он уже был основателем крупной компании по производству тракторов.</p>
			
