			<h2>Гоночные автомобили</h2>
			<p>Ферруччо Ламборгини установил за правило, что автомобили Ламборгини не участвуют в гонках, поэтому в годы его руководства компания не производила гоночных автомобилей.</p>

			<p>Но, уже оказавшись под руководством Розетти, компания вместе с BMW решила создать автомобиль, пригодный для автогонок. Однако Ламборгини со своей частью задачи не справилась, и автомобиль был целиком разработан спортивным отделением BMW. Позднее он поступил в производство и продажу как BMW M1.</p>
			<img src="/lamborghini/labm3.jpg" style="float:right;width:300px;height:200px;">

			<p>В 1986 году для участия в соревнованиях группы «C» компания создала автомобиль Lamborghini QVX. Автомобиль был построен, но проблемы со спонсорами вынудили пропустить сезон. QVX участвовал только в одной непризовой гонке на трассе «Кьялами» (Йоханнесбург) в 1986 году под управлением Тиффа Ниделла (англ. Tiff Needell). Несмотря на то, что автомобиль показал достаточно неплохой результат, программа финансирования была свёрнута.</p>

			<p>С 1989 по 1993 год Ламборгини поставлял двигатели V12 для автомобилей Формула 1. Двигатели поставлялись для Larrousse, Ligier, Lotus, Minardi. В 1991 году Ламборгини выставила в гонках собственную команду, просуществовавшую лишь один сезон. Этот же двигатель, но под логотипом Chrysler (нового владельца компании), в 1993 году тестировался на McLaren. Хотя по результатам тестирований двигатель рекомендовался для использования в сезоне 1994 года, команда предпочла двигатель от Peugeot, и Chrysler закрыл проект.</p>

			<p>Для участия в Diablo Supertrophy — соревнованиях одной модели, проводившихся ежегодно с 1996 по 1999 годы, компания предоставила две модификации автомобиля Lamborghini Diablo — Diablo SVR в первый год и Diablo 6.0 GTR в остальные 3 года.</p>

			<p>Lamborghini разрабатывал Lamborghini Murciélago R-GT для участия в чемпионате FIA GT и 24 часа Ле-Мана. Наилучшие результаты всех этих соревнований — первое место (стартовал последним) в классе LMGT1 на трассе «Каталунья» в серии «Ле-Ман» 2009 г. (автомобиль российской команды «ИнтерПрогрессБанк СПАРТАК Рейсинг») и третье место (стартовал пятым) при открытии чемпионата FIA GT в Валенсии (автомобиль был выставлен командой Reiter Engineering).</p>

			<p>Версия Lamborghini Gallardo для участия в чемпионате FIA GT3 разрабатывается командой Reiter Engineering.</p>
