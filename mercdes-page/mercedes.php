			<h2>Вторая мировая война</h2>
			Во время Второй мировой войны концерн Daimler-Benz выпускал как грузовики, так и легковые автомобили различных классов.
			Однако двухнедельная воздушная бомбардировка англо-американскими ВВС в сентябре 1944-го года превратили Daimler-Benz Aktiengesellschaft в руины.
			Разрушение крупного концерна оценивались по-разному: главный цех в Штутгарте был разрушен на 70 %, двигательный и кузовной цеха в Зинделфингене — 85 %, цех грузовиков в Гаггенау был полностью уничтожен.
			Бывшей фабрике Benz & Cie. в Маннгейме повезло больше всех — всего 20 % разрушения, а завод дизельных двигателей Berlin-Marienfelde, приобретённый в 1902, полностью сравняли с землёй.
			Когда оценки разрушения были готовы к январю 1945-го года, совет директоров постановил, что «Daimler-Benz физически больше не существует».</p>
