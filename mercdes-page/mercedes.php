			<h2>Тюнинг</h2>
			<img src="/mercedes/mercedes1.jpeg" style="float:right;">
			<p>Автомобили марки Mercedes-Benz популярны среди требовательных покупателей, поэтому большое число компаний занимаются настройкой и доводкой автомобилей данного бренда.</p>

			<p>Среди наиболее известных фирм можно выделить следующие:</p>
				<ul>
					<li>AMG — официальное тюнинговое подразделение компании, специализирующееся на высокопроизводительных модификациях заводских моделей марки.
					Двигатели AMG собираются вручную и оснащаются ярлыком с подписью инженера, работавшего над силовым агрегатом. 
					100 % акций предприятия находятся во владении концерна Daimler AG с 1999 года.</li>
					<li>Brabus — немецкое тюнинговое ателье, специализирующееся в первую очередь на автомобилях марки Mercedes-Benz, а также других, выпускаемых концерном Daimler AG, автомобилях, таких как Maybach и Smart.</li>
					<li>Carlsson Autotechnik — немецкая компания, один из ведущих мировых тюнеров в области стайлинга автомобилей.</li>
