			<h2>Новая эпоха</h2>
			<img src="/mercedes/mercnewage.jpg" style="float:right;">
			Модель «600» завершила обновление всего модельного ряда компании.
			Годы выпуска этих автомобилей как раз совпали с расцветом ФРГ как новой экономической силы Западной Европы, что говорит и о масштабах производства и об экспортном успехе автомобилей.
			К середине 1960-х Mercedes-Benz утвердился в лидерах немецкой автопромышленности.
			Разумеется, плавниковая эпоха не завершилась с запуском 600-го, но возможность унифицировать модельный ряд позволила сэкономить огромное количество материальных и людских ресурсов.</p>

			<p>Понтонами и SL Mercedes сумел за 10 лет превратиться из фирмы, находившейся до войны на 170-м месте по выпуску машин, в производителя лучших европейских автомобилей.
			Модели экспортировались во многие страны мира и закупались как знаменитостями, так и политиками.
			Но к концу 1950-х образ современных автомобилей, как и западного общества, резко менялся, и Mercedes-Benz стал авангардом в этой эпохе. 
			В 1959 году пошло в серию новое семейство представительского класса W111, получившее элегантные несущие кузова с вертикальными блоками фар, огромный багажный отсек и независимую подвеску всех колёс (модели 220, 220S, 220SE, 230S, 250SE, 280SE и 280SE 3.5).
			Они продемонстрировали высочайший технический уровень автомобилей этой марки. Главным символом новой эпохи стал квадратный кузов, но с чётким американским влиянием в виде «плавников» на задних крыльях.
			У автомобиля также имелись версии купе и кабриолет. Мода на плавники также перешла на автомобили среднего класса W110.
			В 1961 году Mercedes-Benz выпустил роскошную версию, основанную на 111-м 300SE W112, также имевшую версии купе и кабриолета.</p>
			<img src="/mercedes/mercnewage2.jpg" style="float:right;width:700px;height:400px;">

			<p>Но мода на плавники ушла так же быстро, как и пришла, а компания продолжала вводить новые и более роскошные модели.
			В 1963 году появились две новые модели. Первой стала SL «Пагода» с уникальной крышей (её средняя часть была ниже боковин). Автомобиль выпускался в трёх сериях: 230SL, 250SL и 280SL.
			А в конце 1963-го появился лимузин Mercedes-Benz W100 600.
			Автомобиль имел 6,3-литровый двигатель V8 мощностью 250 л. с., автоматическую 4-ступенчатую коробку передач и пневматическую подвеску колёс. 
			Главное, что у автомобиля почти не было конкурентов, и не только в престиже — несмотря на огромные размеры он мог развивать максимальную скорость до 205 км/ч.
			Выпускались также удлинённые версии Pullman (включая шестидверные варианты) и полукабриолеты — ландоле.</p>
			
			<p>На Франкфуртском автосалоне 1965 года была впервые представлена гамма моделей так называемого S-класса (W108) — самых престижных (после лимузина 600) автомобилей фирмы.
			В него вошли модели 250S и 250SE с 6-цилиндровыми моторами в 150 и 170 л. с., по своим техническим параметрам превосходящие конкурентов. 
			Со временем они получили моторы в 2,8 л, а с 1968 года: 3,5- и 4,5-литровые двигатели V8. 
			Самой мощной и комфортабельной моделью этой серии стала удлинённая W109 300SEL, включая флагман 300SEL 6.3 с 6,3-литровым двигателем от 600-го, развивавшая максимальную скорость 220 км/ч.
			С этого момента серия S стала символом технических достижений компании Mercedes-Benz.</p>

			<p style="border-bottom:2px solid black;padding-bottom:20px;">В 1968 году появились новые модели среднего класса W114 и W115, отличавшиеся модельным рядом двигателей. 
			На последних (230, 250 и 280) стояли шестицилиндровые ДВС, на первых (200, 220 и 240) — четырёхцилиндровые.
			Широкую популярность также получили дизельные комплектации этих моделей. 
			Автомобиль выпускался в версии купе, универсала и удлинённого седана.
			Особенностью серии был тот факт, что её кузов был полностью разработан «с нуля», в отличие от предыдущих, которые в той или иной форме были позаимствованы у прежних моделей.</p>
