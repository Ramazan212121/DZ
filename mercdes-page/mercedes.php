			<h2>Послевоенное время</h2>
				<img src="/mercedes/mercww22.jpg" style="float:right;">
				<p>На послевоенное восстановление разрушенных заводов потребовалось время, поэтому автомобильное производство было начато только в июне 1946 года.
				Для разработки новых автомобилей не было ни технической базы, ни средств, поэтому первым послевоенным автомобилем стал седан W136 — «170V».
				Хотя конструкция была разработана ещё в середине 1930-х годов, малолитражный автомобиль с мотором всего 38 лошадиных сил стал началом новой истории марки.
				Уже с мая 1949 года была проведена крупная модернизация.
				Рабочий объём двигателя увеличен на 70 см3, (соответственно мощность выросла до 52 л. с.; модель «170S»), появились варианты в кузовах кабриолет и универсал (т. н. кабриолеты «А» и «B») и главное — модели с дизельными двигателями «170D».</p>

				<p>К началу 1950-х годов концерн Daimler-Benz строил крупные планы на будущее, однако запуск нового поколения автомобилей требовал дальнейшего развития производственной базы.
				Поэтому в начале 1950-х годов, несмотря на появление новой роскошной серии «300», продолжался выпуск моделей с морально устаревшей конструкцией. 
				Продолжалась постоянная модернизация и запуск новых моделей.
				Так в январе 1952 года появилась модель с увеличенным кузовом, которая получила номер W191[en]. 
				Но ещё до этого, в марте 1951 года, на автомобиль поставили шестицилиндровый двигатель с мощностью 80 л. с. вместо 4-х цилиндрового.
				Вместе с новым дизайном экстерьера (например, расположение передних фонарей в крылья) автомобиль W187[en] получил новое имя «220» и занял средний сегмент между «170»-ми и «300»-ми. Он предлагался в трёх кузовах (седан и кабриолеты «А» и «B»).</p>

				<p>Всего за девять лет (выпуск завершился в сентябре 1955 года) построено соответственно 151 042 и 18 514 автомобилей «170» и «220».
				Благодаря им компания смогла создать прочный фундамент, на котором она станет ведущим автомобильным производителем в Западной Европе.</p>
